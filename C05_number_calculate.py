# 變數設定為數值，Python 會自動視為數字類型
first_num = 6
second_num = 2

# 各種基本數學運算示範
print('加法')        # addition
print(first_num + second_num)
print('減法')        # subtraction
print(first_num - second_num)
print('乘法')        # multiplication
print(first_num * second_num)
print('除法')        # division
print(first_num / second_num)
print('次方')        # exponent
print(first_num ** second_num)  # 6 的 2 次方 = 36
